#include <stdio.h>

void	equation(int n)
{
	for (int a = 0, b = n; a <= n; a++, b--)
		if (a < 100 && b < 100 && a / 10 == b % 10)	
			printf("A = %d, B = %d, C = %d\n", a / 10, a % 10, b / 10);			
}
